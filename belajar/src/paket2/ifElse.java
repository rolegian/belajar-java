package paket2;

public class ifElse {
    public static void main(String[] args){
        int x =10;
        // if(yang ingin diuji)
        if(x>10){
            System.out.print("x lebih besar dari 10");
        } else if(x == 10){
            System.out.print("X sama dengan 10");
        } else {
            System.out.print("X kurang dari 10");
        }
    }
}
